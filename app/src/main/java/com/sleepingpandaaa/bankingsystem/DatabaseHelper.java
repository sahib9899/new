        db.execSQL("insert into user_table values(9696969696,'Parth',8700,'parth@gmail.com')");
        db.execSQL("insert into user_table values(9595959595,'Gautam',3900,'gautam@gmail.com')");
        db.execSQL("insert into user_table values(9494949494,'Harsh',800,'harsh@gmail.com')");
        db.execSQL("insert into user_table values(9393939393,'Ram',1900,'ram@gmail.com')");
        db.execSQL("insert into user_table values(9292929292,'Anushka',8900,'anushka@gmail.com')");
